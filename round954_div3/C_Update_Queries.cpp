#include <bits/stdc++.h>
using namespace std;
#define ll long long

ll binPow (ll a, ll b) {
    ll res = 1;
    while (b > 0) {
        if (b & 1) res = res * a;
        a = a * a;
        b >>= 1;
    }
    return res;
}

ll modInverse(ll A, ll M) {
    ll m0 = M;
    ll y = 0, x = 1;
    if (M == 1) return 0;
    while (A > 1) {
        ll q = A / M;
        ll t = M;
        M = A % M, A = t;
        t = y;
        y = x - q * y;
        x = t;
    }
    if (x < 0) x += m0;
    return x;
}

void solve () {
    ll n, m; cin >> n >> m;
    string s; cin >> s;
    vector <ll> ind (m);
    for (auto &it : ind) cin >> it;
    string c; cin >> c;

    sort (ind.begin(), ind.end());
    sort (c.begin(), c.end());
    
    map <ll, ll> mp;
    for (auto it : ind) mp[it]++;

    vector <ll> ind_2;
    for (auto it : mp) ind_2.emplace_back(it.first);

    for (int i = 0; i < (int)ind_2.size(); i++) {
        s[ind_2[i] - 1] = c[i];
    }

    cout << s << '\n';
}

int main () {
    ios::sync_with_stdio(0);
    cin.tie(0);
    ll _ = 1;
    cin >> _;
    while (_--)
        solve ();
    return 0;
}