#include <bits/stdc++.h>
using namespace std;
#define ll long long

ll binPow (ll a, ll b) {
    ll res = 1;
    while (b > 0) {
        if (b & 1) res = res * a;
        a = a * a;
        b >>= 1;
    }
    return res;
}

ll modInverse(ll A, ll M) {
    ll m0 = M;
    ll y = 0, x = 1;
    if (M == 1) return 0;
    while (A > 1) {
        ll q = A / M;
        ll t = M;
        M = A % M, A = t;
        t = y;
        y = x - q * y;
        x = t;
    }
    if (x < 0) x += m0;
    return x;
}

void solve () {
    ll n; cin >> n;
    string s; cin >> s;

    ll ans_fin = 1e18;
    
    for (int k = 0 ; k < n; k++) {
        if (k + 1 < n) {
            ll ind = k;
            vector <ll> v;
            bool ok = false;

            for (int i = 0; i < n; i++) {
                if (ind == i) {
                    ll act = (long long)((s[i + 1] - '0') + 10 * (s[i] - '0'));
                    v.emplace_back (act);
                    i++;
                }
                else {
                    v.emplace_back (int (s[i] - '0'));
                }
            }

            ll ans = v[0];
            if (v[0] == 1) ans = 0;
            for (ll i = 0; i < v.size(); i++) {
                if (i > 0 and v[i] != 1) ans += v[i];
                if (v[i] == 0) ok = true;
            }
            if (ok) ans = 0;
            if (!ok and ans == 0) ans = 1;
            ans_fin = min (ans_fin, ans);
        }
    }

    std::cout << ans_fin << endl;
}

int main () {
    ios::sync_with_stdio(0);
    cin.tie(0);
    ll _ = 1;
    cin >> _;
    while (_--)
        solve ();
    return 0;
}